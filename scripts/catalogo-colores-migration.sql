-- 1. Crear tabla
CREATE TABLE IF NOT EXISTS telas.catalogo_colores (
  id         SERIAL PRIMARY KEY,
  nombre     text NOT NULL,         -- Código CMYK (ej: "C=0 M=0 Y=100 K=0")
  hex        char(7) NOT NULL,      -- Hexadecimal (ej: "#FFED00")
  r          smallint NOT NULL CHECK (r BETWEEN 0 AND 255),
  g          smallint NOT NULL CHECK (g BETWEEN 0 AND 255),
  b          smallint NOT NULL CHECK (b BETWEEN 0 AND 255),
  c          smallint CHECK (c BETWEEN 0 AND 100),
  m          smallint CHECK (m BETWEEN 0 AND 100),
  y          smallint CHECK (y BETWEEN 0 AND 100),
  k          smallint CHECK (k BETWEEN 0 AND 100),
  familia    text,
  activo     boolean DEFAULT true,
  orden      integer DEFAULT 0,
  created_at timestamptz DEFAULT now()
);

-- 2. Índices
CREATE INDEX IF NOT EXISTS idx_catalogo_colores_familia ON telas.catalogo_colores(familia);
CREATE INDEX IF NOT EXISTS idx_catalogo_colores_activo  ON telas.catalogo_colores(activo);
CREATE UNIQUE INDEX IF NOT EXISTS idx_catalogo_colores_hex ON telas.catalogo_colores(hex);

